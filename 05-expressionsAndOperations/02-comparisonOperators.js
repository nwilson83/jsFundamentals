// Comparison Operators

// Equal
'3' == 3; // notice the values are the same but data types are different

// Strict Equal (checks value and data type, this is used most fo the time)
3 === 3

// Not Equal
'3' != 4;

// Strict Not Equal
'3' !== 3

// Greater Than
3 > 2;

// Less than
2 < 3;

// Greater than or equal too
3 >= 2;

// Less than or equal too
2 <= 3;

// And
2 && 3 // checking for 2 AND 3

// Or
2 || 3 //above return key // checking for 2 OR 3