// Assigment Operators

let x = 5

// Addition Assigment
x += 1; // x = x + 1

// Subtraction Assigment
x -= 1; // x = x - 1

// Multiplication Assigment
x *=1; // x = x * 1

// Division Assigment
x /= 1; // x = x /1

// Remainder (Or Modulus) Assigment
x %= // x = x % 1

// Exponential Assigment
x **= 1 // x = x ** 1